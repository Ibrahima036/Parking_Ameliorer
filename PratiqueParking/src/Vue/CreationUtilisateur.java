    public void affichageTableau(){
        try {
            Connect con = new Connect();
            String [] entete = {"Nom", "Prénom",
                "Email", "Tel", "Mdp", "Pseudo",
                 "Adresse","Type User", "ID"};
            String [] Afficher = new String[15];
            DefaultTableModel model = new DefaultTableModel(null, entete);
            Statement st = con.con.createStatement();
            ResultSet rs = st.executeQuery("select * from utilisateur");
            
            while(rs.next()){
                 Afficher[0] = rs.getString("nom");
                 Afficher[1] = rs.getString("prenom");
                 Afficher[2] = rs.getString("email");
                 Afficher[3] = rs.getString("tel");
                 Afficher[4] = rs.getString("mdp");
                 Afficher[5] = rs.getString("pseudo");
                 Afficher[6] = rs.getString("adresse");
                 Afficher[7] = rs.getString("type_user");
                 Afficher[8] = rs.getString("id");
                 model.addRow(Afficher);
                 
            }
            tableAffichage.setModel(model);
           int i = tableAffichage.getRowCount();
            nbUser.setText(String.valueOf(i));
            
           con.con.close();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Erreur "+ex.getMessage());
        }
        
    }
    

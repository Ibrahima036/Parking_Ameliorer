    private void btnAjouterUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAjouterUserActionPerformed
        //Codage du Boutton Ajouter Un utilisateur
        try {
            Connect con = new Connect();
            String type = (String) ComboUserType.getSelectedItem();
            
         String  select = "select * from utilisateur where email = ? and pseudo = ? and tel = ? and mdp = ?";
         PreparedStatement st = con.con.prepareStatement(select);
         st.setString(1, txtEmailUser.getText());
         st.setString(2, txtPseudoUser.getText());
         st.setString(3, txtTel.getText());
         st.setString(4, txtMdpUser.getText());
         ResultSet r = st.executeQuery();
         if(r.next()){
             JOptionPane.showMessageDialog(this, "Désolé il y'a déja un Utilisateur\n"+
                     "qui à "+
                     "Email : "+r.getString("email")+"\n"
                     +"Pseudo : "+r.getString("pseudo")+"\n"
                     +"Tel : "+r.getString("email")+"\n"
                     + "Veuillez saisir autre que ceux_là+");
         }
            // On vérifie s'il y'a au moins un champ nont requit
         else if(txtNomUser.getText().trim().isEmpty() || txtPrenomUser.getText().trim().isEmpty()
                    || txtTel.getText().trim().isEmpty() || txtMdpUser.getText().trim().isEmpty()
                    || txtAdresse.getText().trim().isEmpty() || txtPseudoUser.getText().trim().isEmpty()
                    || txtEmailUser.getText().trim().isEmpty() || type.trim().isEmpty()){
                JOptionPane.showMessageDialog(this, "Echec d'insertion\n Il ya au moins un champ n'on remplit\n"
                +"SVP veuillez le remplir !!");
            }
            else{
                //établissement de la connection à la base
                
                //Préparation de la reqûte sql
                String sql = "insert into utilisateur(nom, prenom, tel, mdp, email, adresse, pseudo, type_user)"
                        + "values(?, ?, ?, ?, ?, ?, ?, ?)";
                PreparedStatement  pst = (PreparedStatement) con.con.prepareStatement(sql);
                //on set les parametres
                pst.setString(1, txtNomUser.getText());
                pst.setString(2, txtPrenomUser.getText());
                pst.setString(3, txtTel.getText());
                pst.setString(4, txtMdpUser.getText());
                pst.setString(5, txtEmailUser.getText());
                pst.setString(6, txtAdresse.getText());
                pst.setString(7, txtPseudoUser.getText());
                pst.setString(8, (String) ComboUserType.getSelectedItem());
                //Execution de la requete
                int nbupdate = pst.executeUpdate();
                if(nbupdate > 0){
                    JOptionPane.showMessageDialog(this, "Insertion de "+txtNomUser.getText()+" à reussi !!");
                    affichageTableau();
                }else{
                    JOptionPane.showMessageDialog(this, "Echec d'insertion");
                }
                con.con.close();

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erreur ici !!"+e.getMessage());
        }
    }//GEN-LAST:event_btnAjouterUserActionPerformed

